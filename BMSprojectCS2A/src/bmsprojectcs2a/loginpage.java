    private void signinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinActionPerformed
        // TODO add your handling code here:
        if("ENTER CARD NUMBER".equals(card.getText()) || "ENTER 4-DIGIT PIN".equals(pin.getText())){
            JOptionPane.showMessageDialog(this, "ID or Password wrong", "Login Alert", 0);
        }else{
            String cardtext = card.getText().trim();
            String pintext = pin.getText().trim();
            if(!cardtext.matches("\\d+") || !pintext.matches("\\d+")){
                JOptionPane.showMessageDialog(this, "ID or Password wrong", "Login Alert", 1);
            }else{
                SQL db = new SQL();
                ResultSet rs;
                long c = Long.parseLong(card.getText().trim());
                int p = Integer.parseInt(pin.getText().trim());
                rs = db.MatchLoginDetails(c,p);
        
                try{
                    if(rs.next()){
                
                        Dashboard d = new Dashboard(c);
                        d.setVisible(true);
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(this, "ID or Password wrong", "Login Alert", 0);
                    }
                }catch(Exception e){
                    System.out.println(e);
                }
            }
        }
    }//GEN-LAST:event_signinActionPerformed

