private void loadVisualPlots() {

    visualPanel.removeAll();

    sql_model db = new sql_model();
    ResultSet rs = db.getplotrecordsc(); 

    try {
        while (rs.next()) {

            String plot = rs.getString("PLOT_NO");
            String sectionNo = rs.getString("SECTION_NO");
            String status = rs.getString("STATUS");

            JButton box = new JButton(
                "<html><center><b>Plot:</b> " + plot +
                "<br><b>Section:</b> " + sectionNo + "</center></html>"
            );

            box.setFont(new java.awt.Font("Segoe UI", 1, 12));
            box.setFocusPainted(false);

            if (status.equalsIgnoreCase("Available")) {
                box.setBackground(Color.GREEN);
            } else {
                box.setBackground(Color.RED);
                box.setEnabled(false);
            }

            box.addActionListener(e -> {
                plotno.setText(plot);
                section.setText(sectionNo);
            });

            visualPanel.add(box);
        }

    } catch (Exception e) {
        System.out.println(e);
    }

    visualPanel.revalidate();
    visualPanel.repaint();
}
