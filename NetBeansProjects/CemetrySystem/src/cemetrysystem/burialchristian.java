    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
        String datePattern ="^(19[0-9]{2}|20[0-1][0-9]|202[0-5])-(0[1-9]|1[0-2])-(0[1-9]|[12][0-9]|3[01])$";
        Pattern pd = Pattern.compile(datePattern);
        Matcher md = pd.matcher(date.getText());
        
        String measurepattern = "^[0-9]+(\\.[0-9]+)?$";
        Pattern pn = Pattern.compile(measurepattern);

        Matcher mlength = pn.matcher(length.getText());
        Matcher mwidth  = pn.matcher(width.getText());
        Matcher mdepth  = pn.matcher(depth.getText());
        
        if(plotno.getText().equals("")||section.getText().equals("")||date.getText().equals("YYYY/MM/DD")||length.getText().equals("")||width.getText().equals("")||depth.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Missing Details","ERROR",2);
            return;
        } 
       
        if (!md.matches()) {
            JOptionPane.showMessageDialog(this,"Invalid Date format (YYYY-MM-DD)","ERROR",2);
            return;
        }
        
        if (!mlength.matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Length");
            return;
        }
        else if (!mwidth.matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Width");
            return;
        }
        else if (!mdepth.matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Depth");
            return;
        }
        
        sql_model db = new sql_model();
        try{
            int gidc = db.AddBurialC(did,plotno.getText(),section.getText(),date.getText(),length.getText(),width.getText(),depth.getText());
            if(gidc != -1){
                JOptionPane.showMessageDialog(this,"Plot Added Successfully!","SUCCESS",1);
                db.updatestatus(plotno.getText(), section.getText());
                loadVisualPlots();
                payment p = new payment(did, gidc,dname,dcnic,plotno.getText(),section.getText());
                p.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this,"Alert","ERROR",0);
            }
        }catch(Exception e){
          System.out.println(e);
        }

        
    }//GEN-LAST:event_nextActionPerformed

